public class Loopsinjava {
    public static void main(String[] args) {
        int i = 0;
        int j = 0;
        while (i < 5) {
            System.out.println("The value of i is: " + i);
            i++;
        }
        do {
            System.out.println("The value of j is: " + j);
            j++;
        } while (j < 5);

    }
    
}
